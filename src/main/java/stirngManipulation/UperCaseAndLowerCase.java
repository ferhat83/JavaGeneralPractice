package stirngManipulation;

public class UperCaseAndLowerCase {

	public static void main(String[] args) {
		String st = "Hello My Freinds ";
		// we use string methods to convert to upper or lower case
		System.out.println("convert upper case to lower case: "+st.toLowerCase());
		System.out.println("convert lower case to upper case: "+st.toUpperCase());
	}

}
